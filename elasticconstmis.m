function [C, CS] = elasticconstmis(C0,T)

%C(i,j,k,l)=T(i,g)*T(j,h)*C0(g,h,m,n)*T(k,m)*T(l,n)
C=zeros(3,3,3,3);

for i=1:3
    for j=1:3
        for k=1:3
            for l=1:3
                for g=1:3
                    for h=1:3
                        for m=1:3
                            for n=1:3
                C(i,j,k,l)=C(i,j,k,l)+T(i,g)*T(j,h)*C0(g,h,m,n)*T(k,m)*T(l,n);
                            end 
                        end
                    end
                end
            end
        end
    end
end

%Present in the form of 6*6 matrix
CS=[C(1,1,1,1),C(1,1,2,2),C(1,1,3,3),C(1,1,2,3),C(1,1,3,1),C(1,1,1,2);
